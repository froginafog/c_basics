#include <stdio.h>

int main()
{
	int a;
	int b;

	a = 10;
	b = 5;

    if(a < b)
    {
        printf("a is LESS than b\n");
    }

    if(a == b)
    {
        printf("a is EQUAL to b\n");
    }

    if(a > b)
    {
        printf("a is GREATER than b\n");
    }

	return 0;
}

/*
a is GREATER than b
*/
