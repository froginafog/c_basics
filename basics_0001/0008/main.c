#include <stdio.h>

int main()
{
    printf("%d %d %d %d", 10, 20, 30, 40); //print "10 20 30 40" to the screen
    printf("\n"); //go to the next line

    //%d is for formatting an integer
    //each %d is associated with an integer
    //an integer is a whole number (for example, ..., -2, -1, 0, 1, 2, ...)

    return 0;
}


